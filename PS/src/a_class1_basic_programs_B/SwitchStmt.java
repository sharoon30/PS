package a_class1_basic_programs_B;

public class SwitchStmt {
	public static void main(String[] args) {
		String input="apple";
		switch(input) {
		case "apple":System.out.println("it is 10");
		break;
		case "mango": System.out.println("it is 20");
		break;
		case "pineapple": System.out.println("it is 30");
		break;
		default: System.out.println("no options available ");
		break;
		}
		
	}

}
