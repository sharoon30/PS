package k_inheritance;

public class A15_patterns {
	public static void main(String[] args) {
		char i,j;
		
		for(i='A';i<='E';i++) {
			for(j=i;j<'E';j++) {
				System.out.print("  ");
			}
			for(j='A';j<=i;j++) {
				System.out.print(j+" ");
			}System.out.println();
			}
		
	}
	}

